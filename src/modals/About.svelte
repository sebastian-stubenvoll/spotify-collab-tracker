<h1>
what is this project?
<p>
this website can help you keep track of all the activity in your collaborative playlist.
once logged in, you will be able to see who added what song by what artist to which playlist how long ago. and it's all sorted chronologically! just keep scrolling to travel back in time. while you browse the page periodically checks if anything has happened and, if anything has, will automatically adjust your feed and tell you. no refreshing required.
</p>
why is this project a thing?
<p>
there's a good chance this isn't something you want (or need). however if you and peers enjoy actively making use of the collaborative playlist feature then this is supposed to serve you as a fairly easy and comfortable way of keeping up with recently added songs across a large quantity of playlists.
</p>
is this it?
<p>
this is a passion project i work on in my spare time. i am aware the current version is very basic but more features are planned and in development. if you want more information or are looking to contribute feel free to check out the <a href='https://github.com/sebastian-stubenvoll/spotify-collab-tracker' target='_blank'>the github page</a>.
</p>
</h1>


